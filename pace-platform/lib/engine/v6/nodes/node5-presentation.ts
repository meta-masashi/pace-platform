/**
 * Node 5 実行モジュール: プレゼンテーション & 監査ログ。
 *
 * 以下の処理を行う:
 * 1. PipelineOutput の組み立て
 * 2. InferenceTraceLog の構築
 * 3. NLG テンプレートベースのサマリーテキスト生成
 * 4. 法的免責条項の付与
 */
export const node5Presentation: NodeExecutor<
  PresentationInput,
  PresentationOutput
> = {
  nodeId: 'node5_presentation',

  async execute(
    input: PresentationInput,
    context: AthleteContext,
    config: PipelineConfig,
  ): Promise<NodeResult<PresentationOutput>> {
    const startMs = performance.now();
    const warnings: string[] = [];

    const traceId = generateTraceId();
    const timestamp = new Date().toISOString();

    // ----- Step 1: PipelineOutput の組み立て -----
    const pipelineOutput: PipelineOutput = {
      traceId,
      athleteId: context.athleteId,
      timestamp,
      decision: input.decision,
      featureVector: input.featureVector,
      inference: input.inference,
      dataQuality: input.dataQuality,
      pipelineVersion: PIPELINE_VERSION,
    };

    // ----- Step 2: InferenceTraceLog の構築 -----
    const traceLog: InferenceTraceLog = {
      traceId,
      athleteId: context.athleteId,
      orgId: context.orgId,
      timestampUtc: timestamp,
      pipelineVersion: PIPELINE_VERSION,
      inferenceSnapshot: {
        inputs: input.cleanedInput,
        appliedConstants: {
          thresholds: config.thresholds,
          ewma: config.ewma,
          preparedness: config.preparedness,
        },
        calculatedMetrics: input.featureVector,
        bayesianComputation: input.inference,
        triggeredRule: input.decision.priority,
        decision: input.decision.decision,
        decisionReason: input.decision.reason,
        overridesApplied: input.decision.overridesApplied,
        nodeResults: input.nodeResults,
      },
    };

    // ----- Step 3: NLG サマリー生成 -----
    const nlgSummary = generateNLGSummary(
      input.decision,
      input.featureVector,
      input.dataQuality,
    );

    // ----- 警告: データ品質が低い場合 -----
    if (input.dataQuality.qualityScore < 0.5) {
      warnings.push(
        `データ品質スコアが低い（${Math.round(input.dataQuality.qualityScore * 100)}%）ため、判定の信頼性が低下しています。`,
      );
    }

    return {
      nodeId: 'node5_presentation',
      success: true,
      executionTimeMs: performance.now() - startMs,
      data: {
        pipelineOutput,
        traceLog,
        nlgSummary,
      },
      warnings,
    };
  },
};
