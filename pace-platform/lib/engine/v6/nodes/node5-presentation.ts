/** Node 5 の出力データ */
export interface PresentationOutput {
  /** パイプライン最終出力 */
  pipelineOutput: PipelineOutput;
  /** 推論トレースログ（DB 保存用） */
  traceLog: InferenceTraceLog;
  /** NLG サマリーテキスト */
  nlgSummary: string;
}

/** Node 5 の入力データ */
export interface PresentationInput {
  /** 判定結果（Node 4） */
  decision: DecisionOutput;
  /** 推論結果（Node 3） */
  inference: InferenceOutput;
  /** 特徴量ベクトル（Node 2） */
  featureVector: FeatureVector;
  /** データ品質レポート（Node 1） */
  dataQuality: DataQualityReport;
  /** クリーニング済み入力データ */
  cleanedInput: DailyInput;
  /** 各ノードの実行結果サマリー */
  nodeResults: Record<
    NodeId,
    { success: boolean; executionTimeMs: number; warnings: string[] }
  >;
}

