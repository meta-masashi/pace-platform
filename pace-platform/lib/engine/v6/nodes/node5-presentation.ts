const MATURATION_LABELS: Record<string, string> = {
  safety: '安全モード（データ蓄積不足）',
  learning: '学習モード（ベースライン構築中）',
  full: '完全推論モード',
};

/**
 * NLG テンプレートベースの判定サマリーテキストを生成する。
 *
 * テンプレート構造:
 *   [判定色アイコン] コンディション判定: [判定色ラベル]
 *   判定根拠: [優先度ラベル]
 *   [判定理由テキスト]
 *   ---
 *   データ品質: [品質スコア]% | モード: [成熟モード]
 *   ACWR: [値] | 単調性: [値] | プレパレッドネス: [値]
 *   ---
 *   [免責条項]
 *
 * @param decision - 判定結果
 * @param featureVector - 特徴量ベクトル
 * @param dataQuality - データ品質レポート
 * @returns NLG サマリーテキスト
 */
function generateNLGSummary(
  decision: DecisionOutput,
  featureVector: FeatureVector,
  dataQuality: DataQualityReport,
): string {
  const colorIcon = DECISION_COLOR_ICONS[decision.decision];
  const colorLabel = DECISION_LABELS[decision.decision];
  const priorityLabel = PRIORITY_LABELS[decision.priority];
  const maturationLabel =
    MATURATION_LABELS[dataQuality.maturationMode] ??
    dataQuality.maturationMode;
  const qualityPercent = Math.round(dataQuality.qualityScore * 100);

  const lines: string[] = [
    `${colorIcon} コンディション判定: ${colorLabel}`,
    `判定根拠: ${priorityLabel}`,
    '',
    decision.reason,
    '',
    '---',
    `データ品質: ${qualityPercent}% | モード: ${maturationLabel}`,
    `ACWR: ${featureVector.acwr.toFixed(2)} | 単調性: ${featureVector.monotonyIndex.toFixed(2)} | プレパレッドネス: ${featureVector.preparedness.toFixed(2)}`,
  ];

  // オーバーライドがある場合
  if (decision.overridesApplied.length > 0) {
    const overrideLabels = decision.overridesApplied.map(
      (o) => OVERRIDE_LABELS[o] ?? o,
    );
    lines.push(`適用オーバーライド: ${overrideLabels.join('、')}`);
  }

  lines.push('---');
  lines.push(LEGAL_DISCLAIMER);

  return lines.join('\n');
}

/** 判定色アイコン */
const DECISION_COLOR_ICONS: Record<InferenceDecision, string> = {
  RED: '[RED]',
  ORANGE: '[ORANGE]',
  YELLOW: '[YELLOW]',
  GREEN: '[GREEN]',
};

/** オーバーライドの日本語ラベル */
const OVERRIDE_LABELS: Record<string, string> = {
  game_day: '試合日',
  acclimatization: '順化期間',
  weight_making: '減量期',
  pipeline_fallback: 'パイプラインフォールバック',
};

